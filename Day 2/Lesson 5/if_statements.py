# ---------- If Statements ----------
"""
Conditions

if it is hot
    It is a hot day
    Drink plenty of water
otherwise if it is cold
    It is a cold day
    Wear warm clothes
otherwise
    It is a lovely day


"""

is_hot = False
is_cold = True
if is_hot:
    print("It is a hot day")
    print("Drink plenty of water")
elif is_cold:
    print("It is a cold day")
    print("Wear warm clothes")
else:
    print("It is a lovely day")